#Write a procedure that takes a string of words separated by spaces (assume no punctuation or capitaliza-tion), together with a "target" word, and shows the position of the target word in the string of words. For example, if the string is:

#we dont need no education we dont need no thought control no we dontand the target is the word "dont" then your procedure should return the list 1, 6, 13 because "dont" appears at the 1st, 6th, and 13th position in the string. (We start counting positions of words in the string from 0.) Your procedure should return False if the target word doesnâ€™t appear in the string.


def procedure(string,target):
    words=string.split(" ") #turn the string into a list of words
    solution=[] #list that will be displayed
    for i in range(len(words)):
            if words[i]==target: solution.append(i)
    if len(solution)==0: return False
    return solution

